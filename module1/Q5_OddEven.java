package com.module1;

public class Q5_OddEven 
{
	public static void main(String[] args) 
	{
		int no = -1;
		
		if(no%2 == 0)
		{
			System.out.println(no+ " is Even");
		}
		else if(no%2 == 1)
		{
			System.out.println(no+ " is Odd");
		}
		else
		{
			System.out.println(no+ " is Negative ");
		}
	}
}
